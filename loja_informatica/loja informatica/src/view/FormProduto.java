    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Produto p = new Produto();
        p.setNome(txtNome.getText());
        p.setPreco(Double.parseDouble(txtPreco.getText()));
        p.setQuantidade(Integer.parseInt(spnQuantidade.getValue().toString()));
        
        Categoria cat = (Categoria)cbxCategoria.getSelectedItem();
        p.setCategoria_id(cat.getId());
        p.setCategoria_nome(cat.getNome());
        
        int res = -1;
        
        if(txtId.getText().isEmpty())
        {
            res = new ProdutoDAO().inserir(p);
        }    
        else
        {    
            p.setId(Integer.parseInt(txtId.getText()));
            res = new ProdutoDAO().atualizar(p);
            
        }    
        
        if (res < 0)
            JOptionPane.showMessageDialog(null, "Não foi possível realizar a operação");
        
        else if (res == 0)
             JOptionPane.showMessageDialog(null, "Produto atualizado.");    
        else
        {    
            txtId.setText(String.valueOf(res));
            JOptionPane.showMessageDialog(null, "Inserção realizada com sucesso!");
        }    
        
        txtId.setText("");
        txtNome.setText("");
        txtPreco.setText("");
        spnQuantidade.setValue(0);
        cbxCategoria.setSelectedIndex(0);
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void txtNomePKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomePKeyReleased
        String chave = txtNomeP.getText();
        txtTotal.setText("");
        if (!chave.isEmpty())
        {
            Double total = 0.0;
            List<Produto> lista = new ProdutoDAO().pesquisarPorNome(chave);
            configurarTabela();
            
            DefaultTableModel m = (DefaultTableModel)tabPesquisa.getModel();
            
            for(Produto p : lista)
            {
                m.addRow(new Object[]{
                         p.getId(), p.getNome(), p.getPreco(), p.getQuantidade(),
                         p.getCategoria_nome(), p.getTotal()});
                total += p.getTotal();
            }
            tabPesquisa.setModel(m);
            txtTotal.setText(total.toString());
        }
        else
            configurarTabela();
    }//GEN-LAST:event_txtNomePKeyReleased

