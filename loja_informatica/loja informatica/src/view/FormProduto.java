    private void tabPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabPesquisaMouseClicked
        if(evt.getClickCount() == 2){
            int linha = tabPesquisa.getSelectedRow();
        
        
        String id = tabPesquisa.getValueAt(linha, 0).toString();
        String nome = tabPesquisa.getValueAt(linha, 1).toString();
        String preco = tabPesquisa.getValueAt(linha, 2).toString();
        String categoria = tabPesquisa.getValueAt(linha, 4).toString();
        Produto c = new Produto();
        txtId.setText(id);
        txtNome.setText(nome);
        txtPreco.setText(preco);
        spnQuantidade.setValue(tabPesquisa.getValueAt(linha,3));
        DefaultComboBoxModel m = (DefaultComboBoxModel)cbxCategoria.getModel();
        int i;
        for (i = 0; i < m.getSize(); i++)
        {
            Categoria d = (Categoria)m.getElementAt(i);
            if (d.getNome().equalsIgnoreCase(categoria))
                break;
        }    
        cbxCategoria.setSelectedIndex(i);
        pnProduto.setSelectedIndex(1);
        
        configurarTabela();
        }
    }//GEN-LAST:event_tabPesquisaMouseClicked

