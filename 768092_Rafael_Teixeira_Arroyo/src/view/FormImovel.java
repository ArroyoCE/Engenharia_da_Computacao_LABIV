    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
            List<Imovel> lista = new ImovelDAO().listar();
            

            DefaultTableModel m = (DefaultTableModel)tabLista.getModel();

            for(Imovel i : lista)
            {
                m.addRow(new Object[]{
                    i.getId(), i.getProprietario(), i.getTelefone(), i.getTipo(),
                    i.getValor(), i.getStatus()});
            
        }
        tabLista.setModel(m);
       
        
    
    }//GEN-LAST:event_btnListarActionPerformed

