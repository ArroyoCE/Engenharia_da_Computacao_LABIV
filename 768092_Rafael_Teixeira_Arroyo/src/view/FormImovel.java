    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Imovel i = new Imovel();
        i.setProprietario(txtProprietario.getText());
        i.setTelefone(txtTelefone.getText());

        i.setTipo((String) cbxTipo.getSelectedItem());

        i.setValor(Double.parseDouble(txtValor.getText()));

        i.setStatus((String) cbxStatus.getSelectedItem());

        int res = -1;

        if(txtId.getText().isEmpty())
        {
            res = new ImovelDAO().inserir(i);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Imóvel já inserido no sistema.");
        }

        if (res < 0)
        JOptionPane.showMessageDialog(null, "Não foi possível realizar a operação");

        else if (res == 0)
        JOptionPane.showMessageDialog(null, "Produto atualizado.");
        else
        {
            txtId.setText(String.valueOf(res));
            JOptionPane.showMessageDialog(null, "Inserção realizada com sucesso!");
        }

        txtId.setText("");
        txtProprietario.setText("");
        txtValor.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

